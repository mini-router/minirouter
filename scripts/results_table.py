#!/usr/bin/env python3
"""Aggregate all eval JSONs under experiments/ into a structured results table.

Reads every ``experiments/**/eval*.json`` written by ``trinity.eval``, and emits:
  - a per-eval Markdown table (TRINITY vs best single vs random, per coordinator),
  - a multi-task summary (TRINITY per-task-best avg vs best fixed single model avg),
  - the R1/R2/R4 invariant verdicts,
  - oracle-ceiling rows from ``experiments/**/oracle_report_*.json`` when present.

Run on the box where experiments/ lives:
    python scripts/results_table.py            # Markdown to stdout
    python scripts/results_table.py --json      # also dump consolidated results.json
"""
from __future__ import annotations

import argparse
import glob
import json
import math
from pathlib import Path


def load_rows(root: str = "experiments") -> list[dict]:
    rows = []
    for p in sorted(glob.glob(f"{root}/**/eval*.json", recursive=True)):
        try:
            d = json.load(open(p))
        except Exception:
            continue
        r = d.get("results", {})
        singles = {k.split("::", 1)[1]: v for k, v in r.items() if k.startswith("single::")}
        if not singles or "TRINITY" not in r:
            continue
        best_model = max(singles, key=singles.get)
        rows.append({
            "file": p,
            "benchmark": d.get("benchmark", "?"),
            "coordinator": Path(p).parent.name,
            "variant": Path(p).stem,  # eval / eval_fixed / *_rigorous
            "trinity": r.get("TRINITY"),
            "random": r.get("random_routing"),
            "best_single": singles[best_model],
            "best_model": best_model,
            "singles": singles,
        })
    return rows


def load_oracle_reports(root: str = "experiments") -> list[dict]:
    """Load oracle-ceiling report JSONs produced by ``scripts/oracle_ceiling.py --analyze``."""
    reports: list[dict] = []
    for p in sorted(glob.glob(f"{root}/**/oracle_report_*.json", recursive=True)):
        try:
            with open(p, encoding="utf-8") as fh:
                d = json.load(fh)
        except (OSError, json.JSONDecodeError):
            continue
        if not isinstance(d.get("point_estimates"), dict) or not isinstance(d.get("verdict"), dict):
            continue
        reports.append({**d, "file": p})
    return reports


def fmt(x) -> str:
    return f"{x:.3f}" if isinstance(x, (int, float)) else "—"


def render_oracle_section(reports: list[dict]) -> str:
    """Markdown table summarizing pool headroom from oracle-ceiling reports."""
    if not reports:
        return ""
    out = ["\n## Oracle-ceiling diagnostics\n"]
    out.append(
        "From `experiments/**/oracle_report_<bench>.json` "
        "(see `scripts/oracle_ceiling.py`).\n"
    )
    out.append(
        "| benchmark | best single | routing oracle | headroom | "
        "gap closed | verdict |"
    )
    out.append("|---|---|---|---|---|---|")
    for rep in reports:
        pe = rep["point_estimates"]
        tri = rep.get("trinity") or {}
        gap = tri.get("router_gap_closed")
        if gap is None or (isinstance(gap, float) and math.isnan(gap)):
            gap_s = "—"
        else:
            gap_s = fmt(gap)
        verdict = rep.get("verdict", {}).get("label", "?")
        out.append(
            f"| {rep.get('benchmark', '?')} | {fmt(pe.get('best_single'))} | "
            f"{fmt(pe.get('routing_oracle'))} | {fmt(pe.get('routing_headroom'))} | "
            f"{gap_s} | {verdict} |"
        )
    return "\n".join(out) + "\n"


def render(rows: list[dict], oracle_reports: list[dict] | None = None) -> str:
    out = ["# TRINITY replication — results\n"]
    out.append("Auto-generated by `scripts/results_table.py` from `experiments/**/eval*.json`.\n")

    out.append("## Per-coordinator held-out evals\n")
    out.append("| benchmark | coordinator | variant | TRINITY | best single (model) | random | TRINITY>best? | TRINITY>random? |")
    out.append("|---|---|---|---|---|---|---|---|")
    for r in rows:
        gt_best = "✅" if (r["trinity"] or 0) > (r["best_single"] or 0) else ("≈" if abs((r["trinity"] or 0) - (r["best_single"] or 0)) <= 0.03 else "❌")
        gt_rand = "✅" if (r["trinity"] or 0) > (r["random"] or 0) else "❌"
        out.append(f"| {r['benchmark']} | {r['coordinator']} | {r['variant']} | "
                   f"{fmt(r['trinity'])} | {fmt(r['best_single'])} ({r['best_model']}) | "
                   f"{fmt(r['random'])} | {gt_best} | {gt_rand} |")

    # Multi-task summary: best TRINITY per benchmark, and best fixed single model averaged.
    by_bench: dict[str, list[dict]] = {}
    for r in rows:
        by_bench.setdefault(r["benchmark"], []).append(r)
    benches = sorted(by_bench)
    if len(benches) >= 2:
        out.append("\n## Multi-task summary (the paper's R1/R2 claim)\n")
        # union of single models across benches
        models = sorted({m for r in rows for m in r["singles"]})
        # best per-bench single score for each model (max across that bench's evals)
        def single_avg(model):
            vals = []
            for b in benches:
                bench_vals = [r["singles"].get(model) for r in by_bench[b] if model in r["singles"]]
                bench_vals = [v for v in bench_vals if v is not None]
                if bench_vals:
                    vals.append(sum(bench_vals) / len(bench_vals))
            return sum(vals) / len(vals) if vals else None
        # TRINITY per-task best (max TRINITY across coordinators per bench), averaged
        trin_avg = sum(max(r["trinity"] for r in by_bench[b]) for b in benches) / len(benches)
        rand_avg = sum(
            sum(r["random"] for r in by_bench[b]) / len(by_bench[b]) for b in benches
        ) / len(benches)
        out.append(f"Averaged across {benches}:\n")
        out.append("| system | multi-task average |")
        out.append("|---|---|")
        out.append(f"| **TRINITY (per-task best coordinator)** | **{trin_avg:.3f}** |")
        for m in models:
            sa = single_avg(m)
            if sa is not None:
                out.append(f"| single: {m} (fixed) | {sa:.3f} |")
        out.append(f"| random routing | {rand_avg:.3f} |")
        best_fixed = max((single_avg(m) or 0) for m in models)
        out.append(f"\n**R1/R2** (TRINITY avg > best fixed single avg): "
                   f"{'✅ HOLDS' if trin_avg > best_fixed else '❌'} "
                   f"({trin_avg:.3f} vs {best_fixed:.3f})")
        out.append(f"**R4** (TRINITY avg > random avg): "
                   f"{'✅ HOLDS' if trin_avg > rand_avg else '❌'} ({trin_avg:.3f} vs {rand_avg:.3f})")
    out.append(render_oracle_section(oracle_reports or []))
    return "\n".join(out) + "\n"


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--root", default="experiments")
    ap.add_argument("--json", action="store_true", help="also write results.json")
    args = ap.parse_args()
    rows = load_rows(args.root)
    oracle_reports = load_oracle_reports(args.root)
    print(render(rows, oracle_reports))
    if args.json:
        Path("experiments/results.json").write_text(
            json.dumps({"eval_rows": rows, "oracle_reports": oracle_reports}, indent=2)
        )


if __name__ == "__main__":
    main()
